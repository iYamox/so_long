#include "../../include/so_long.h"

void	ft_putnbr(int nb)
{
	char	c;

	if (nb <= 9)
	{
		c = nb + '0';
		write(1, &c, 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	return ;
}
